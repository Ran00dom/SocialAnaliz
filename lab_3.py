import networkx as nx

# Задание 3. Требуется вычислить в программе среднюю степень вершины и сравнить 
# её со значением средней степени вершины, полученной по формуле из материала лекций. 
# Требуется написать скрипт для генерации графа в модели Эрдёша-Реньи с заданными характеристиками.

# Ввод значений n и p
n = 70
p = 0.45

# Генерация графа
G = nx.erdos_renyi_graph(n, p)

# Вычисление средней степени вершины в графе
a = 0
for i in G.nodes():
    a = a + G.degree(i)
average_degree = float(a) / len(G.nodes())

# Теоретическая средняя степень вершины из лекций: (n-1)*p
theoretical_average_degree = (n - 1) * p

# Вывод результатов
print(f"Средняя степень вершины в сгенерированном графе: {average_degree:.4f}")
print(f"Теоретическая средняя степень вершины: {theoretical_average_degree:.4f}")

# Сравнение
difference = abs(average_degree - theoretical_average_degree)
print(f"Разница между вычисленной и теоретической средней степенью: {difference:.4f}")

# Результат
# Средняя степень вершины в сгенерированном графе: 30.8000
# Теоретическая средняя степень вершины: 31.0500
# Разница между вычисленной и теоретической средней степенью: 0.2500